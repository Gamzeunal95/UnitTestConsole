namespace MathLib
{
    public class DortIslem
    {
        public int Toplama(int x, int y)
        {
            return x + y;
        }

        public int Cikartma(int x, int y)
        {
            return x - y;
        }

        public int Bolme(int x, int y)
        {
            return x / y;
        }

        public int Carpma(int x, int y)
        {
            return x * y;
        }
    }
}
